def add(num1: float=0, num2: float=0):
    return num1+num2

def sub(num1: float=0, num2: float=0):
    return num1-num2


def mul(num1: float=0, num2: float=0):
    return num1*num2


def floordiv(num1: float=0, num2: float=0):
    return num1//num2


def div(num1: float=0, num2: float=0):
    return num1/num2


def modulo(num1: float=0, num2: float=0):
    return num1 % num2


def sqrt(num1: float=0):
    return num1**0.5


def square(num1: float=0):
    return num1**2


def cube(num1: float=0):
    return num1**3


def power(num1: float=0, num2: float=0):
    return num1**num2

